% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

prefix = get(handles.edit1,'String');
surfix = get(handles.edit2,'String');
test_set_begin = str2double(get(handles.edit3,'String'));
test_set_end = str2double(get(handles.edit4,'String'));
interval = str2double(get(handles.edit5,'String'));
rsf = str2double(get(handles.edit6,'String'));

imgt=struct('cdata',[],'colormap',[]);
for n=test_set_begin:interval:test_set_end
  imgt((n-test_set_begin+interval)/interval).cdata=imread([prefix,num2str(n,'%3.3i'),surfix]);
  imgt((n-test_set_begin+interval)/interval).cdata=imresize(imgt((n-test_set_begin+interval)/interval).cdata,rsf);
  [imgt((n-test_set_begin+interval)/interval).cdata,imgt((n-test_set_begin+interval)/interval).colormap] = gray2ind(imgt((n-test_set_begin+interval)/interval).cdata,256 );
  
end

[file,path]=uiputfile('*.mat');
save([path,file],'imgt');







